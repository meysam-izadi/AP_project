public class Date {
    private int year;
    private int month;
    private int day;
    public Date(){

    }
    public Date(int year) {
        this.year = year;
    }
    public Date(int year , int month) {
        this.year = year;
        this.month = month;
    }
    public Date(int year , int month , int day) {
        this.year = year;
        this.month=month;
        this.day=day;
    }
}
